public class java_04_else_if {
    public static void main(String[] args) {
        int a = 202, b = 412, c = 809;
        if (a > b) {
            System.out.println(a);
        }
        else if (b > c) {
            System.out.println(b);
        }
        else {
            System.out.println(c);
        }
    }
}
