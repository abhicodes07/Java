public class java_01_methods {
    static int Max(int x, int y) {
        if (x > y) {
            return 1;
        }
        return 0;
    }
    public static void main(String[] args) {
        System.out.println(Max(12, 45));
        System.out.println(Max(1000, 45));
        System.out.println(Max(70, 23));
    }
}
